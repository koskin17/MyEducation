    # üîÅ –ó–∞ –∫—É–ª—ñ—Å–∞–º–∏ Python –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î –∑–≤–µ—Ä–Ω–µ–Ω–Ω—è –¥–æ `balance` –Ω–∞ –≤–∏–∫–ª–∏–∫–∏ –º–µ—Ç–æ–¥—ñ–≤.
