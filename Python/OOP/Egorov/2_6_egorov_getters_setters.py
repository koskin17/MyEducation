# Property –∏ –º–µ—Ç–æ–¥—ã getters –∏ setters

class BankAccount:
    def __init__(self, name, balance):
        self.name = name
        self.__balance = balance

    def get_balance(self):
        return self.__balance

    def set_balance(self, value):
        # –î–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã –ø–µ—Ä–µ–¥–∞–≤–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –±—ã–ª–æ —á–∏c–ª–æ–º, —Ç.–µ. —ç–∫–∑–µ–º–ø–ª—è—Ä–æ–º —á–∏—Å–ª–∞,
        # –ø–µ—Ä–µ–¥ —É—Å—Ç–∞–Ω–æ–≤–∫–æ–π –≤ –º–µ—Ç–æ–¥–µ setter –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è –ø—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∏–Ω–∞–¥–ª–µ–∂–Ω–æ—Å—Ç–∏ value
        # –∫ —Ç–∏–ø—É –¥–∞–Ω–Ω—ã—Ö int –∏ float.
        # –í –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –∏—Å–∫–ª—é—á–µ–Ω–∏–µ —Å —Å–æ–æ–±—â–µ–Ω–∏–µ–º
        if not isinstance(value, (int, float)):
            raise ValueError("–ë–∞–ª–∞–Ω—Å –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å —á–∏—Å–ª–æ–º")
        else:
            self.__balance = value

    # –ü—Ä–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –º–µ—Ç–æ–¥–æ–≤ getter –∏ setter –≤ –∫–æ–¥–µ –Ω—É–∂–Ω–æ –∏—Ö –≤—ã–∑—ã–≤–∞—Ç—å
    # –î–ª—è —Ç–æ–≥–æ, —á—Ç–æ–±—ã —É–ø—Ä–æ—Å—Ç–∏—Ç—å –∑–∞–¥–∞—á—É –∏ –ø–æ–ª—É—á–∞—Ç—å –¥–æ—Å—Ç—É–ø –∫ –∑–∞—â–∏—â–µ–Ω–Ω—ã–º –∞—Ç—Ä–∏–±—É—Ç–∞–º, –∞ —Ç–∞–∫–∂–µ
    # —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—Ç—å –∏–º –Ω–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–≤–æ—ë—Å—Ç–≤–æ Property
    # –î–ª—è —ç—Ç–æ–≥–æ –≤–Ω—É—Ç—Ä–∏ –∫–ª–∞—Å—Å–∞ —Å–æ–∑–¥–∞—ë—Ç—Å—è –∏–º—è —Å–≤–æ–π—Å—Ç–≤–∞ –∏ –≤–Ω—É—Ç—Ä–∏ –ø—Ä–æ—Å—Ç–∞–≤–ª—è—é—Ç—Å—è 2 –∞—Ç—Ç—Ä–∏–±—É—Ç–∞
    # fget (getter) –∏ fset (setter) –∏ –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∑–Ω–∞—á–µ–Ω–∏–π —ç—Ç–∏—Ö –∞—Ç—Ä–∏–±—É—Ç–æ–≤ —É–∫–∞–∑—ã–≤–∞—é—Ç—Å—è –∏–º–µ–Ω—è –º–µ—Ç–æ–¥–æ–≤

    # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è –º–µ—Ç–æ–¥ —É–¥–∞–ª–µ–Ω–∏—è –∞—Ç—Ç—Ä–∏–±—É—Ç–∞. –°–æ–∑–¥–∞—ë—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è, –∞ –≤ —Å–≤–æ–π—Å—Ç–≤–æ –¥–æ–±–∞–≤–ª—è–µ—Ç—Å—è fdel —Å
    # —É–∫–∞–∑–∞–Ω–∏–µ–º –∏–º–µ–Ω–∏ –º–µ—Ç–æ–¥–∞
    def delete_balance(self):
        del self.__balance

    balance = property(fget=get_balance, fset=set_balance, fdel=delete_balance)
    # –¶–µ–π –∫–æ–¥ —Å—Ç–≤–æ—Ä—é—î **–≤–ª–∞—Å—Ç–∏–≤—ñ—Å—Ç—å** (property) —É –∫–ª–∞—Å—ñ Python, —è–∫–∞ –Ω–∞–∑–∏–≤–∞—î—Ç—å—Å—è `balance`.
    # ---
    # ### üîç –©–æ —Ç–∞–∫–µ `property`?
    # `property` ‚Äî —Ü–µ —Å–ø–µ—Ü—ñ–∞–ª—å–Ω–∏–π –º–µ—Ö–∞–Ω—ñ–∑–º —É Python, —è–∫–∏–π –¥–æ–∑–≤–æ–ª—è—î **–∑–≤–µ—Ä—Ç–∞—Ç–∏—Å—è –¥–æ –º–µ—Ç–æ–¥—ñ–≤ —è–∫ –¥–æ –∑–≤–∏—á–∞–π–Ω–∏—Ö –∑–º—ñ–Ω–Ω–∏—Ö**. –í—ñ–Ω –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î—Ç—å—Å—è –¥–ª—è **—ñ–Ω–∫–∞–ø—Å—É–ª—è—Ü—ñ—ó** ‚Äî –∫–æ–ª–∏ –º–∏ —Ö–æ—á–µ–º–æ –∫–æ–Ω—Ç—Ä–æ–ª—é–≤–∞—Ç–∏ –¥–æ—Å—Ç—É–ø –¥–æ –∞—Ç—Ä–∏–±—É—Ç—ñ–≤ (–Ω–∞–ø—Ä–∏–∫–ª–∞–¥, –ø–µ—Ä–µ–≤—ñ—Ä—è—Ç–∏ –∞–±–æ –∑–º—ñ–Ω—é–≤–∞—Ç–∏ –∑–Ω–∞—á–µ–Ω–Ω—è —Ç—ñ–ª—å–∫–∏ –∑–∞ –ø–µ–≤–Ω–∏–º–∏ –ø—Ä–∞–≤–∏–ª–∞–º–∏).
    # ---
    # ### üîß –£ —Ç–≤–æ—î–º—É –ø—Ä–∏–∫–ª–∞–¥—ñ:
    # * `fget=get_balance` ‚Äî —Ñ—É–Ω–∫—Ü—ñ—è (–º–µ—Ç–æ–¥), —è–∫–∞ –±—É–¥–µ –≤–∏–∫–ª–∏–∫–∞—Ç–∏—Å—å, –∫–æ–ª–∏ —Ç–∏ –∑–≤–µ—Ä—Ç–∞—î—à—Å—è –¥–æ `balance`
    # * `fset=set_balance` ‚Äî —Ñ—É–Ω–∫—Ü—ñ—è, —è–∫–∞ –±—É–¥–µ –≤–∏–∫–ª–∏–∫–∞—Ç–∏—Å—å, –∫–æ–ª–∏ —Ç–∏ –ü–†–ò–°–í–û–Æ–Ñ–® –∑–Ω–∞—á–µ–Ω–Ω—è `balance`
    # * `fdel=delete_balance` ‚Äî —Ñ—É–Ω–∫—Ü—ñ—è, —è–∫–∞ –±—É–¥–µ –≤–∏–∫–ª–∏–∫–∞—Ç–∏—Å—å, –∫–æ–ª–∏ —Ç–∏ –í–ò–î–ê–õ–Ø–Ñ–® `balance`
