''' Функция deal_cards раздает заданное количество карт
из колоды '''
def deal_cards(deck, number):
    ''' создаём накопитель для количества карт на курах '''
    hand_value = 0

    '''Проверяем и убеждаемся, что кол-во карт для раздачи
        не больше кол-ва карт в колоде '''
    if number > len(deck):
        number = len(deck)

    ''' Раздаём карты и накапливаем их значения '''
    for count in range(number):
        card = random.choice(list(deck.keys()))
        print(card)
        hand_value += deck[card]
        deck.pop(card)
        

    ''' Показываем величину карт на руках '''
    print('Величина карт на руках: ', hand_value)
    print('Оставшаяся колода:\n', deck)

''' Вызываем главную функцию '''
main()
