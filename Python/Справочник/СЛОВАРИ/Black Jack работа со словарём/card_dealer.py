''' Раздача случайных карт из колоды и запоминание их кол-ва на руках.
    Программа разбита на три этапа при помощи трёх функций.
    1. Сразу вызывается функция, которая вызывает функцию create_deck() для создания колоды карт.
    Эта функция возвращает колоду карт в виде словаря "Ключ : Значение", который присваивается переменной deck.
    Для наглядности этого словарь выводится на экран.
    2. Зарашивается у пользователя кол-во карт, которые надо раздать и введённое кол-во присваивается переменной num_cards.
    3. Вызывается функция deal_cards(), которая получает словарь из карт, созданный в пункте 1, и кол-во розданных карт.
    4. Функция deal_cards сразу создаёт переменную hand_value для подсчёта номинала карт на руках с начальных значением 0.
    После этого проверяется, сколько карт осталось в колоде и сколько карт запрашивает пользователь. Если пользователь запросил карт больше, чем есть в колоде,
    то запрошенное кол-во приравнивается к оставшемуся в колоде кол-ву карт, чтобы не раздать больше, чем есть.
    После этого сохдаётся цикл подсчета суммы достоинств карт, которые розданы пользователю:
        - переменное card присваивается случайная карта из колоды (из словаря с парами "ключ(карта):значение(кол-во очков). Для этого используется метод random.choice с переводом
        словаря в список ключей "на лету". В результате словарь становится списком ключей и из этого списка рандомно выбирается одно значение (ключ) - карта;
        - полученная карта выводится на экран;
        - в переменную hand_value, которая была создана ранее для посчёта суммы номиналов карт на руках, добавляются очки карты, которые выираеются из словаря методом обращения к нему
        по ключу. Сам словарь (колода карт) остался без изменений потому-что он форматировался в список "на лету", без внесения изменений в него;
        - после этого на экран выводится сумма очков карт, которые на руках и оставшаяся колода(словарь) для сравнения. В колоде нет карт, которые были розданы.
    '''

import random

''' Объявлям главную функцию '''
def main():
    ''' Создаём колоду карт, вызывая функцию create_deck '''
    deck = create_deck()
    print('Первоначальная колода\n', deck)

    ''' Получаем количество карт для раздачи '''
    num_cards = int(input('Сколько карт раздать? '))

    ''' Раздаём карты, вызывая функцию '''
    deal_cards(deck, num_cards)

