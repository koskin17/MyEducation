# 's3ooOOooDy' has exams. He wants to study hard this time. He has an array of studying hours per day for the previous exams. He wants to know the length of the maximum non-decreasing contiguous subarray of the studying days, to study as much before his current exams.

# Example:

# For a = [2,2,1,3,4,1] the answer is 3.

# [input] array.integer a

# The number of hours he studied each day.

# [output] integer

# The length of the maximum non-decreasing contiguous subarray.

# –£ 's3ooooooooDy' —ç–∫–∑–∞–º–µ–Ω—ã. –í —ç—Ç–æ—Ç —Ä–∞–∑ –æ–Ω —Ö–æ—á–µ—Ç —Ö–æ—Ä–æ—à–æ —É—á–∏—Ç—å—Å—è. –£ –Ω–µ–≥–æ –µ—Å—Ç—å –º–∞—Å—Å–∏–≤ —É—á–µ–±–Ω—ã—Ö —á–∞—Å–æ–≤ –≤ –¥–µ–Ω—å –¥–ª—è –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö —ç–∫–∑–∞–º–µ–Ω–æ–≤. –û–Ω —Ö–æ—á–µ—Ç —É–∑–Ω–∞—Ç—å –¥–ª–∏–Ω—É –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –Ω–µ—É–±—ã–≤–∞—é—â–µ–≥–æ —Å–º–µ–∂–Ω–æ–≥–æ –ø–æ–¥–º–∞—Å—Å–∏–≤–∞ —É—á–µ–±–Ω—ã—Ö –¥–Ω–µ–π, —á—Ç–æ–±—ã –∑–∞–Ω–∏–º–∞—Ç—å—Å—è —Å—Ç–æ–ª—å–∫–æ –∂–µ –ø–µ—Ä–µ–¥ —Ç–µ–∫—É—â–∏–º–∏ —ç–∫–∑–∞–º–µ–Ω–∞–º–∏.

# –ü—Ä–∏–º–µ—Ä:

# –î–ª—è a = [2,2,1,3,4,1] –æ—Ç–≤–µ—Ç - 3. [–≤—Ö–æ–¥] array.integer a –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –æ–Ω –∑–∞–Ω–∏–º–∞–ª—Å—è –∫–∞–∂–¥—ã–π –¥–µ–Ω—å.

# [output] integer –î–ª–∏–Ω–∞ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –Ω–µ—É–±—ã–≤–∞—é—â–µ–≥–æ —Å–º–µ–∂–Ω–æ–≥–æ –ø–æ–¥–º–∞—Å—Å–∏–≤–∞.
def max_non_decreasing_subarray(a):
    max_length = 1
    current_length = 1
    
    for i in range(1, len(a)):
        if a[i] >= a[i - 1]:  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∏–¥–µ—Ç –ª–∏ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –≤–≤–µ—Ä—Ö –∏–ª–∏ –Ω–∞ –æ–¥–Ω–æ–º —É—Ä–æ–≤–Ω–µ
            current_length += 1
        else:
            max_length = max(max_length, current_length)
            current_length = 1  # –ù–∞—á–∏–Ω–∞–µ–º –Ω–æ–≤—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å
    
    return max(max_length, current_length)

# –ü—Ä–∏–º–µ—Ä –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è:
a = [2, 2, 1, 3, 4, 1]
print(max_non_decreasing_subarray(a))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: 3

