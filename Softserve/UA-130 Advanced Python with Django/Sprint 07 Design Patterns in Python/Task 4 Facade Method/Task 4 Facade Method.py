class Washer:   #Class for wash the clothes
    def wash(self):
        print("Washing...")

class Rinser:   # Class for rinse the clothes
    def rinse(self):
        print("Rinsing...")

class Spinner:  # Class for spin the clothes
    def spin(self):
        print("Spinning...")

# Facade
class WashingMachine:   # Class as Facade
    def __init__(self):
        self.washer = Washer()
        self.rinser = Rinser()
        self.spinner = Spinner()

    def startWashing(self):
        self.washer.wash()
        self.rinser.rinse()
        self.spinner.spin()

# Code for client
if __name__ == "__main__":
    machine = WashingMachine()
    machine.startWashing()

