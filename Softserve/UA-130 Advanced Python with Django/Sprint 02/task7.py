### **–ö–æ–¥ –Ω–∞ Python:**

import re

def pretty_message(data):
    pattern = r"\b(?=\S*0[a-z]{4}\b)\S+\b"
    return re.findall(pattern, data)

# –¢–µ—Å—Ç–æ–≤—ã–µ –ø—Ä–∏–º–µ—Ä—ã
data1 = "0msdfgh 00000xbcd 0bbcd7 hjkj00wjhg hjkj0ajhg"
print(pretty_message(data1))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: ['00000xbcd', 'hjkj00wjhg', 'hjkj0ajhg']

data2 = "In0 programm0ing, regular0 express0ions (re0gex) are0 a0 powerful0 tool0 for0 text0 process0ing."
print(pretty_message(data2))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: ['express0ions']

data3 = "0Regular0 expressions0 provide0 a0 flexible0 and0 concise0 way0 to0 se0arch and0 manipu0late text0 data0 in0 str0ings."
print(pretty_message(data3))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: ['se0arch', 'manipu0late', 'str0ings']

data4 = "0alpha 0beta 0gamma 0delta"
print(pretty_message(data4))  # –û–∂–∏–¥–∞–µ–º—ã–π –≤—ã–≤–æ–¥: ['0beta']

