# 2. **`\d*`** ‚Üí –õ—é–±–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ü–∏—Ñ—Ä (`0-9`) **–¥–æ –ø–æ—è–≤–ª–µ–Ω–∏—è –Ω—É–ª—è** (–º–æ–∂–µ—Ç –±—ã—Ç—å –ø—É—Å—Ç—ã–º).
# 3. **`0`** ‚Üí –û–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π **–Ω–æ–ª—å –ø–µ—Ä–µ–¥ –±—É–∫–≤–∞–º–∏**.
# 4. **`[a-z]{4}`** ‚Üí –†–æ–≤–Ω–æ **4 —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã** (`a-z`) –≤ –∫–æ–Ω—Ü–µ —Å–ª–æ–≤–∞.
